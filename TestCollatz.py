from io import StringIO
from unittest import main, TestCase

from Collatz import collatz_read, collatz_eval, collatz_print, collatz_solve

# -----------
# TestCollatz
# -----------


class TestCollatz (TestCase):
    # ----
    # read
    # ----

    def test_read(self):
        s = "1 10\n"
        i, j = collatz_read(s)
        self.assertEqual(i,  1)
        self.assertEqual(j, 10)

    # ----
    # eval
    # ----

    def test_eval_1(self):
        v = collatz_eval(1, 1)  #test whether boundaries of range are set correctly
        self.assertEqual(v, 1)

    def test_eval_2(self):
        v = collatz_eval(1, 7)  #tests whether boundaries are inclusive
        self.assertEqual(v, 17)

    def test_eval_3(self):
        v = collatz_eval(3, 2)
        self.assertEqual(v, 8)

    def test_eval_4(self):
        v = collatz_eval(1,1000000)
        self.assertEqual(v, 525)

    # -----
    # print
    # -----

    def test_print(self):
        w = StringIO()
        collatz_print(w, 1, 10, 20)
        self.assertEqual(w.getvalue(), "1 10 20\n")

    # -----
    # solve
    # -----

    def test_solve(self):
        r = StringIO("1 2\n1 10\n11 15\n1 15\n")
        w = StringIO()
        collatz_solve(r, w)
        self.assertEqual(
            w.getvalue(), "1 2 2\n1 10 20\n11 15 18\n1 15 20\n")

# ----
# main
# ----

if __name__ == "__main__":
    main()

